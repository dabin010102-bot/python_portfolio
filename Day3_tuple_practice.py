# 튜플 인덱싱
t1 = (1, 2, 'a', 'b')
print(t1[0])  # 1
print(t1[3])  # 'b'

# 튜플 슬라이싱
t1 = (1, 2, 'a', 'b')
print(t1[1:])  # (2, 'a', 'b')

# 튜플 더하기
t1 = (1, 2, 'a', 'b')
t2 = (3, 4)
t3 = t1 + t2
print(t3)  # (1, 2, 'a', 'b', 3, 4)

# 튜플 반복
t2 = (3, 4)
t3 = t2 * 3
print(t3)  # (3, 4, 3, 4, 3, 4)

# 튜플 길이 구하기
t1 = (1, 2, 'a', 'b')
print(len(t1))  # 4
